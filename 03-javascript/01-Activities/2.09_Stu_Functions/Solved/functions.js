var multiplier = function(num1){
    return num1 * num1
}

var sentencer = function(string1, string2){
    return string1 + string2
}

var whatsForLunch = function (string1, num1, string2){
    return string1 + num1 + string2
}

console.log(multiplier(4));

console.log(sentencer("I ", "did it!"));

console.log(whatsForLunch("I will have ", 1, "entire pizza please!"));